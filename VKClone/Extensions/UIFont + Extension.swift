import UIKit

extension UIFont {
    
    static func tahomaBold20() -> UIFont? {
        return UIFont(name: "Tahoma-Bold", size: 20)
    }
    
    static func tahomaBold17() -> UIFont? {
        return UIFont(name: "Tahoma-Bold", size: 17)
    }
    
    static func tahoma17() -> UIFont? {
        return UIFont(name: "Tahoma", size: 17)
    }
    
    static func tahoma16() -> UIFont? {
        return UIFont(name: "Tahoma", size: 16)
    }
    
    static func tahoma15() -> UIFont? {
        return UIFont(name: "Tahoma", size: 15)
    }
    
    static func tahoma14() -> UIFont? {
        return UIFont(name: "Tahoma", size: 14)
    }
    
    static func tahoma13() -> UIFont? {
        return UIFont(name: "Tahoma", size: 13)
    }
    
    static func tahoma12() -> UIFont? {
        return UIFont(name: "Tahoma", size: 12)
    }
}
